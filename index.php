	<h2>Le projet de l'ATA:</h2>
	
	<h3>Projet de développement de l’aquaponie sur l’agglomération toulousaine et ses zones périphériques</h3>
	<p>
		L’Association Toulousaine d’Aquaponie (<strong>ATA</strong>) créée à l’automne 2016 regroupe des passionnés de l’aquaponie réunis autour d’un projet innovant, respectueux de l’environnement, créateur d’emplois et de valeurs humaines.</br>
		Elle prévoit la mise en place d’un premier site début 2017 qui sera le point de départ de la création d’une dizaine de fermes aquaponiques fonctionnant en réseau pour répondre aux besoins diversifiés des consommateurs locaux en poissons et légumes.</br>
		Sur ce premier site, nous prévoyons de répondre à plusieurs objectifs :</br>
		o	Formation sur l’aquaponie et la bioponie via une zone de démonstration.</br>
		o	Expérimentations et analyses pour améliorer nos systèmes de production et disposer de références techniques utiles à l’installation des autres fermes.</br>
		o	Production de divers types de poissons et de légumes pour répondre à la demande locale. Valorisation des sous-produits par lombri-compostage.</br>
		o	Transformation des produits avec conditionnement  en frais dans un premier temps.</br>
		o	Commercialisation des produits préparés vers divers circuits courts (vente directe principalement puis d’autres marchés locaux).</br>
		o	Animations autour de ce mode de production respectueux de l’environnement et développement de lien social.</br>
		Dans ces perspectives, l’aide des particuliers, associations, entreprises et financeurs institutionnels (Région, département, structures intercommunales) va permettre d’enclencher une dynamique autour de l’aquaponie valorisant différents types de sites non utilisés : anciennes serres horticoles, friches industrielles, fonciers autour d’anciennes gravières, divers emplacements dans et autour de bâtiments urbains.</br>
		<strong>Dès maintenant :</strong></br>
		>	Adhérez à l’Association Toulousaine d’Aquaponie(ATA) pour participer à ce projet novateur et créateur d’activités durables (bulletin d’adhésion au verseau)</br>
		>	Soyez associés à l’évolution de notre projet (animations, dégustations de produits, lancement de nouvelles fermes…)</br>
	</p>
